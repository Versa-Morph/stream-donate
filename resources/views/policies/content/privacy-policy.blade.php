    <h2><span class="iconify" data-icon="solar:lock-bold-duotone"></span>Pasal 1 - Pendahuluan dan Ruang Lingkup</h2>
    <p>Kebijakan Privasi ini merupakan bagian tidak terpisahkan dari Ketentuan Layanan StreamDonate dan menjelaskan secara komprehensif bagaimana PT VersaMorph Teknologi Indonesia ("Perusahaan", "Kami", "Data Controller") mengumpulkan, menggunakan, memproses, menyimpan, dan melindungi Data Pribadi Anda sebagai pengguna platform donasi digital StreamDonate ("Platform").</p>
    
    <p>Perusahaan berkomitmen penuh untuk menghormati dan melindungi hak privasi Anda sebagaimana dijamin oleh Undang-Undang Nomor 27 Tahun 2022 tentang Perlindungan Data Pribadi ("UU PDP") dan peraturan pelaksanaannya. Kebijakan Privasi ini mengatur:</p>
    <ul>
        <li>Jenis Data Pribadi yang kami kumpulkan dari Anda;</li>
        <li>Tujuan dan dasar hukum pemrosesan Data Pribadi;</li>
        <li>Bagaimana kami menggunakan, menyimpan, dan mengamankan Data Pribadi Anda;</li>
        <li>Siapa saja yang dapat mengakses dan menerima Data Pribadi Anda;</li>
        <li>Hak-hak Anda terkait Data Pribadi dan cara将其行使;</li>
        <li>Kebijakan penggunaan cookie dan teknologi pelacakan serupa.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:database-bold-duotone"></span>Pasal 2 - Data Pribadi yang Kami Kumpulkan</h2>
    
    <h3>2.1 Data yang Anda Berikan Secara Langsung</h3>
    <p>Ketika Anda mendaftar, menggunakan, atau berinteraksi dengan Platform kami, Anda secara sukarela memberikan informasi berikut:</p>
    <ul>
        <li><strong>Data Identitas:</strong> Nama lengkap sesuai kartu identitas resmi, nama pengguna (username), tanggal lahir, jenis kelamin, dan foto profil;</li>
        <li><strong>Data Kontak:</strong> Alamat email yang valid, nomor telepon seluler dengan kode negara Indonesia (+62), dan alamat domisili;</li>
        <li><strong>Data Verifikasi:</strong> Nomor KTP atau paspor untuk proses verifikasi identitas (KYC), serta dokumen pendukung lainnya yang diperlukan untuk compliance dengan regulasi keuangan;</li>
        <li><strong>Data Keuangan:</strong> Informasi rekening bank, e-wallet, atau metode pembayaran lain yang Anda daftarkan untuk menerima atau memberikan donasi;</li>
        <li><strong>Data Transaksi:</strong> Riwayat donasi, jumlah donasi, pesan dukungan, dan metadata terkait aktivitas donasi Anda;</li>
        <li><strong>Data Komunikasi:</strong> Konten pesan, umpan balik, dan komunikasi lain yang Anda kirimkan kepada kami melalui berbagai channel.</li>
    </ul>

    <h3>2.2 Data yang Dikumpulkan Secara Otomatis</h3>
    <p>Ketika Anda mengakses atau menggunakan Platform, kami secara otomatis mengumpulkan informasi teknis dan usage data, termasuk namun tidak terbatas pada:</p>
    <ul>
        <li><strong>Data Teknis:</strong> Alamat IP (Internet Protocol), jenis dan versi browser, sistem operasi, device identifier, dan konfigurasi zona waktu;</li>
        <li><strong>Data Penggunaan:</strong> URL referral, halaman yang dikunjungi, fitur yang digunakan, waktu akses, durasi kunjungan, dan pola navigasi;</li>
        <li><strong>Data Lokasi:</strong> Perkiraan lokasi geografis berdasarkan IP address untuk keperluan keamanan dan lokalisasi layanan;</li>
        <li><strong>Data Log:</strong> File log server yang merekam aktivitas, error, dan insiden teknis yang terjadi selama penggunaan Platform.</li>
    </ul>

    <h3>2.3 Data dari Sumber Pihak Ketiga</h3>
    <p>Kami dapat menerima Data Pribadi Anda dari sumber-sumber pihak ketiga yang sah, termasuk:</p>
    <ul>
        <li>Institusi keuangan dan payment gateway untuk verifikasi transaksi dan pencegahan penipuan;</li>
        <li>Layanan verifikasi identitas pihak ketiga untuk keperluan KYC (Know Your Customer);</li>
        <li>Platform media sosial (jika Anda memilih untuk login atau menghubungkan akun media sosial);</li>
        <li>Partner bisnis dan affiliate yang bekerja sama dengan kami untuk penyediaan layanan.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:settings-bold-duotone"></span>Pasal 3 - Tujuan dan Dasar Hukum Pemrosesan</h2>
    
    <h3>3.1 Tujuan Pemrosesan</h3>
    <p>Kami memproses Data Pribadi Anda untuk tujuan-tujuan berikut:</p>
    <ul>
        <li><strong>Penyediaan Layanan:</strong> Memproses dan memfasilitasi transaksi donasi, mengelola akun pengguna, dan menyediakan fitur-fitur Platform;</li>
        <li><strong>Verifikasi dan Keamanan:</strong> Memverifikasi identitas pengguna, mendeteksi dan mencegah penipuan, pencucian uang, dan aktivitas ilegal lainnya;</li>
        <li><strong>Dukungan Pelanggan:</strong> Merespons pertanyaan, keluhan, dan permintaan dukungan teknis melalui berbagai channel komunikasi;</li>
        <li><strong>Peningkatan Layanan:</strong> Menganalisis pola penggunaan untuk meningkatkan kualitas, fungsionalitas, dan pengalaman pengguna di Platform;</li>
        <li><strong>Kepatuhan Regulasi:</strong> Memenuhi kewajiban hukum dan regulasi yang berlaku, termasuk pelaporan kepada otoritas terkait;</li>
        <li><strong>Komunikasi Pemasaran:</strong> Mengirimkan informasi tentang produk, fitur baru, dan promosi (dengan persetujuan eksplisit Anda).</li>
    </ul>

    <h3>3.2 Dasar Hukum Pemrosesan</h3>
    <p>Sesuai dengan UU PDP, dasar hukum yang kami gunakan untuk memproses Data Pribadi Anda meliputi:</p>
    <ul>
        <li><strong>Persetujuan (Consent):</strong> Anda telah memberikan persetujuan secara eksplisit untuk pemrosesan Data Pribadi untuk tujuan tertentu;</li>
        <li><strong>Pelaksanaan Perjanjian:</strong> Pemrosesan diperlukan untuk melaksanakan perjanjian dengan Anda atau untuk mengambil langkah atas permintaan Anda sebelum签订 perjanjian;</li>
        <li><strong>Kewajiban Hukum:</strong> Pemrosesan diperlukan untuk mematuhi kewajiban hukum yang melekat pada Perusahaan;</li>
        <li><strongKepentingan Sah:</strong> Pemrosesan diperlukan untuk kepentingan sah Perusahaan, dengan catatan tidak mengabaikan hak dan kebebasan fundamental Anda.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:server-bold-duotone"></span>Pasal 4 - Penyimpanan dan Keamanan Data</h2>
    
    <h3>4.1 Lokasi Penyimpanan</h3>
    <p>Data Pribadi Anda disimpan di server yang berlokasi di Indonesia dan/atau cloud infrastructure yang memenuhi standar keamanan internasional. Untuk Data Pribadi sensitif, kami menggunakan fasilitas penyimpanan dengan sertifikasi keamanan yang sesuai.</p>

    <h3>4.2 Standar Keamanan</h3>
    <p>Kami menerapkan langkah-langkah keamanan yang komprehensif untuk melindungi Data Pribadi Anda, termasuk:</p>
    <ul>
        <li><strong>Enkripsi:</strong> Penggunaan protokol TLS (Transport Layer Security) untuk transmisi data dan enkripsi AES-256 untuk data at rest;</li>
        <li><strong>Access Control:</strong> Penerapan prinsip least privilege dan role-based access control, dengan otentikasi multi-faktor untuk akses sensitif;</li>
        <li><strong>Monitoring:</strong> Sistem monitoring 24/7 untuk mendeteksi dan merespons insiden keamanan secara real-time;</li>
        <li><strong>Backup:</strong> Pencadangan data secara berkala dengan enkripsi dan penyimpanan di lokasi yang terpisah;</li>
        <li><strong>Audit:</strong> Review keamanan berkala oleh tim security internal dan audit eksternal independen.</li>
    </ul>

    <h3>4.3 Retensi Data</h3>
    <p>Kami menyimpan Data Pribadi Anda hanya selama diperlukan untuk memenuhi tujuan pemrosesan atau selama periode yang dipersyaratkan oleh hukum yang berlaku. Setelah periode retensi berakhir, Data Pribadi akan dihapus atau dianonimisasi sesuai dengan prosedur yang berlaku.</p>

    <h2><span class="iconify" data-icon="solar:share-bold-duotone"></span>Pasal 5 - Pengungkapan dan Transfer Data</h2>
    
    <h3>5.1 Kategori Penerima</h3>
    <p>Kami dapat membagikan Data Pribadi Anda kepada kategori pihak berikut:</p>
    <ul>
        <li><strong>Pemroses Pembayaran:</strong> Bank, e-wallet providers, dan payment gateway untuk pemrosesan transaksi donasi;</li>
        <li><strong>Creator:</strong> Informasi donasi yang Anda berikan, termasuk nama (jika tidak anonym) dan pesan dukungan;</li>
        <li><strong>Provider Teknologi:</strong> Vendor pihak ketiga yang menyediakan infrastruktur IT, hosting, dan layanan cloud;</li>
        <li><strong>Konsultan Profesional:</strong> Akuntan, auditor, penasihat hukum, dan konsultan lain yang membantu operasional bisnis;</li>
        <li><strong>Otoritas Berwenang:</strong> instansi pemerintah terkait sesuai kewajiban hukum, termasuk namun tidak terbatas pada OJK, PPATK, dan Kepolisian.</li>
    </ul>

    <h3>5.2 Transfer Internasional</h3>
    <p>Jika Data Pribadi ditransfer ke luar wilayah Indonesia, kami akan memastikan bahwa transfer tersebut sesuai dengan ketentuan UU PDP tentang transfer data lintas batas dan bahwa tingkat perlindungan data yang memadai tersedia di negara tujuan.</p>

    <h2><span class="iconify" data-icon="solar:user-bold-duotone"></span>Pasal 6 - Hak Anda sebagai Subjek Data</h2>
    <p>Sesuai dengan UU PDP, Anda memiliki hak-hak berikut terkait Data Pribadi Anda:</p>
    <ul>
        <li><strong>Hak Akses:</strong> Mendapatkan konfirmasi apakah Data Pribadi Anda diproses dan mengakses Data Pribadi tersebut beserta informasinya;</li>
        <li><strong>Hak Koreksi:</strong> Meminta koreksi atas Data Pribadi yang tidak akurat, tidak lengkap, atau tidak mutakhir;</li>
        <li><strong>Hak Penghapusan:</strong> Meminta penghapusan Data Pribadi dalam kondisi tertentu sebagaimana diatur dalam UU PDP;</li>
        <li><strong>Hak Penarikan Persetujuan:</strong> Menarik persetujuan yang telah Anda berikan sebelumnya, tanpa mempengaruhi keabsahan pemrosesan sebelumnya;</li>
        <li><strong>Hak Keberatan:</strong> Mengajukan keberatan terhadap pemrosesan Data Pribadi untuk tujuan tertentu;</li>
        <li><strong>Hak Portabilitas:</strong> Menerima Data Pribadi Anda dalam format yang terstruktur dan dapat dibaca mesin.</li>
    </ul>

    <p>Untuk行使 hak-hak tersebut, silakan submit permintaan melalui email ke privacy@tiptipan.id dengan subjek "Permintaan Hak Subjek Data". Kami akan merespons permintaan Anda dalam waktu maksimal 1x24 jam untuk permintaan sederhana dan 3x24 jam untuk permintaan yang kompleks.</p>

    <h2><span class="iconify" data-icon="solar:cookie-bold-duotone"></span>Pasal 7 - Kebijakan Cookie</h2>
    <p>Kami menggunakan cookie dan teknologi pelacakan serupa untuk berbagai keperluan, termasuk:</p>
    <ul>
        <li><strong>Cookie Esensial:</strong> Diperlukan untuk pengoperasian dasar Platform, seperti autentikasi dan security;</li>
        <li><strong>Cookie Preferensi:</strong> Mengingat preferensi dan pengaturan Anda untuk meningkatkan pengalaman browsing;</li>
        <li><strong>Cookie Analitik:</strong> Mengumpulkan data anonim tentang penggunaan Platform untuk keperluan statistik dan perbaikan layanan;</li>
        <li><strong>Cookie Marketing:</strong> Melacak aktivitas browsing untuk menampilkan iklan yang relevan (dengan persetujuan).</li>
    </ul>
    <p>Anda dapat mengelola preferensi cookie melalui pengaturan browser Anda atau melalui Cookie Consent Manager yang tersedia di Platform.</p>

    <h2><span class="iconify" data-icon="solar:shield-warning-bold-duotone"></span>Pasal 8 - Notifikasi Breach</h2>
    <p>Jika terjadi pelanggaran terhadap Data Pribadi (data breach) yang dapat mengakibatkan risiko tinggi bagi hak dan kebebasan Anda, kami akan:</p>
    <ul>
        <li>Memberitahu Anda dalam waktu paling lambat 1x24 jam setelah kami mengetahui terjadinya pelanggaran;</li>
        <li>Menyampaikan informasi tentang sifat pelanggaran, kategori dan jumlah Data Pribadi yang terdampak;</li>
        <li>Mendeskripsikan langkah-langkah yang telah dan akan kami ambil untuk mengatasi pelanggaran;</li>
        <li>Melaporkan insiden kepada otoritas pengawas (Kominfo) sesuai ketentuan UU PDP.</li>
    </ul>

