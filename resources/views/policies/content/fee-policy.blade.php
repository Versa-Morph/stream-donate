    <h3>2.3 Catatan Biaya Payment Gateway</h3>
    <p>Biaya payment gateway bervariasi tergantung metode pembayaran yang digunakan oleh Donatur. Secara umum:</p>
    <ul>
        <li><strong>E-Wallet (GoPay, OVO, DANA):</strong> Sekitar 2% dari jumlah transaksi</li>
        <li><strong>Kartu Kredit:</strong> Sekitar 2.5% dari jumlah transaksi</li>
        <li><strong>Transfer Bank:</strong> Rp 4.000 per transaksi tetap</li>
        <li><strong>Gerai Retail:</strong> Rp 2.500 per transaksi tetap</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:box-bold-duotone"></span>Pasal 3 - Biaya Produk Digital</h2>
    
    <h3>3.1 Kebijakan Harga</h3>
    <p>Produk digital yang dijual melalui Platform StreamDonate memiliki kebijakan harga sebagai berikut:</p>
    <ul>
        <li><strong>Tidak ada biaya platform tambahan:</strong> Harga produk digital sudah mencakup seluruh biaya operasional terkait;</li>
        <li><strong>Sudah termasuk PPN:</strong> Sesuai regulasi perpajakan Indonesia, seluruh harga produk digital sudah mencakup Pajak Pertambahan Nilai sebesar 11% (sesuai PMK terbaru);</li>
        <li><strong>Harga transparan:</strong> Harga yang ditampilkan adalah harga final yang harus dibayar, tanpa biaya tersembunyi.</li>
    </ul>

    <h3>3.2 Kebijakan Refund</h3>
    <p>Pengembalian dana untuk produk digital mengikuti ketentuan dalam Kebijakan Pengembalian Dana yang dapat diakses di halaman Kebijakan Pengembalian Dana.</p>

    <h2><span class="iconify" data-icon="solar:gift-bold-duotone"></span>Pasal 4 - Layanan Gratis</h2>
    <p>Perusahaan bangga dapat menyediakan layanan-layanan berikut secara cuma-cuma bagi seluruh Pengguna:</p>
    <ul>
        <li><strong>Pembuatan Akun:</strong> Gratis untuk semua Pengguna tanpa batasan</li>
        <li><strong>Pemeliharaan Akun:</strong> Tidak dikenakan biaya bulanan atau tahunan</li>
        <li><strong>Fitur Dasar:</strong> Alert donasi, leaderboard, dan widget standar tersedia tanpa biaya</li>
        <li><strong>Penarikan Dana (Metode Standar):</strong> Tidak dikenakan biaya admin untuk transfer bank ke rekening utama Creator</li>
        <li><strong>Dukungan Pelanggan:</strong> Tim support tersedia 24/7 tanpa biaya tambahan</li>
        <li><strong>Dashboard Analytics:</strong> Akses data statistik dan laporan donasi gratis</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:dollar-circle-bold-duotone"></span>Pasal 5 - Biaya Tambahan yang Mungkin Timbul</h2>
    
    <h3>5.1 Biaya yang Mungkin Dikenakan</h3>
    <p>Dalam kondisi tertentu, biaya berikut mungkin dikenakan:</p>
    <ul>
        <li><strong>Biaya Penarikan Instan:</strong> Untuk Creator yang memilih payout instan di luar jadwal reguler, dikenakan biaya admin sebesar Rp 2.500 - 5.000;</li>
        <li><strong>Biaya Konversi Mata Uang:</strong> Untuk transaksi dari luar Indonesia dengan mata uang asing, dikenakan biaya konversi yang mengikuti kurs tengah Bank Indonesia;</li>
        <li><strong>Biaya Chargeback:</strong> Untuk setiap chargeback yang terbukti tidak sah, dikenakan biaya administrasi sebesar Rp 50.000 per kasus.</li>
    </ul>

    <h3>5.2 Tidak Ada Biaya Tersembunyi</h3>
    <p>Perusahaan berkomitmen untuk tidak mengenakan biaya apapun yang tidak telah diinformasikan secara jelas kepada Pengguna sebelum transaksi dilakukan. Pengguna selalu akan melihat rincian biaya sebelum menyelesaikan setiap transaksi.</p>

    <h2><span class="iconify" data-icon="solar:clock-bold-duotone"></span>Pasal 6 - Mekanisme Pemberitahuan Perubahan</h2>
    
    <h3>6.1 Pemberitahuan</h3>
    <p>Perubahan terhadap struktur biaya akan diinformasikan kepada Pengguna melalui:</p>
    <ul>
        <li><strong>Email:</strong> Pemberitahuan dikirim ke alamat email yang terdaftar minimal 30 (tiga puluh) hari kalender sebelum perubahan berlaku;</li>
        <li><strong>Notifikasi Platform:</strong> Pengumuman melalui dashboard Creator dan notifikasi push;</li>
        <li><strong>Pengumuman Publik:</strong> Melalui website resmi StreamDonate di bagian Kebijakan.</li>
    </ul>

    <h3>6.2 Persetujuan</h3>
    <p>Dengan continued menggunakan Platform setelah perubahan biaya diumumkan, Pengguna dianggap telah menyetujui struktur biaya yang baru. Jika Pengguna tidak menyetujui perubahan, Pengguna dapat menghentikan penggunaan Platform.</p>

