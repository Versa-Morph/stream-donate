<div class="policy-content">
    <h2><span class="iconify" data-icon="solar:film-bold-duotone"></span>Pasal 1 - Pendahuluan dan Tujuan</h2>
    <p>Kebijakan Panduan Konten ini merupakan dokumen resmi yang menetapkan standar perilaku dan kualitas konten yang diharapkan dari seluruh Pengguna Platform StreamDonate yang dioperasikan oleh PT VersaMorph Teknologi Indonesia ("Perusahaan"). Kebijakan ini dibuat untuk menciptakan lingkungan digital yang aman, positif, dan produktif bagi seluruh anggota komunitas StreamDonate.</p>

    <p>Tujuan kebijakan ini adalah untuk:</p>
    <ul>
        <li>Mewujudkan lingkungan digital yang bebas dari pelecehan, diskriminasi, dan konten berbahaya;</li>
        <li>Melindungi hak dan kebebasan seluruh Pengguna dalam mengekspresikan diri secara wajar;</li>
        <li>Menjaga integritas dan reputasi Platform StreamDonate;</li>
        <li>Mematuhi peraturan perundang-undangan yang berlaku di Republik Indonesia.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:book-bold-duotone"></span>Pasal 2 - Prinsip Dasar Komunitas</h2>
    
    <h3>2.1 Nilai-Nilai Inti</h3>
    <p>Seluruh Pengguna diharapkan untuk menjunjung tinggi nilai-nilai berikut dalam berinteraksi di Platform:</p>
    <ul>
        <li><strong>Menghormati:</strong> Perlakukan setiap Pengguna lain dengan hormat dan sopan santun, terlepas dari perbedaan latar belakang, pandangan, atau keyakinan;</li>
        <li><strong>Berkomunikasi dengan Baik:</strong> Gunakan bahasa yang santun, konstruktif, dan membangun dalam setiap interaksi;</li>
        <li><strong>Bertanggung Jawab:</strong> Ambil tanggung jawab penuh atas setiap tindakan, komentar, dan konten yang Anda publikasikan di Platform;</li>
        <li><strong>Integritas:</strong> Berikan informasi yang akurat dan tidak menyesatkan; jangan memanipulasi sistem atau pengguna lain.</li>
    </ul>

    <h3>2.2 Standar Perilaku</h3>
    <p>Pengguna diharapkan untuk:</p>
    <ul>
        <li>Berpartisipasi secara positif dalam komunitas</li>
        <li>Melaporkan konten atau perilaku yang melanggar</li>
        <li>Menghormati privasi pengguna lain</li>
        <li>Tidak menggunakan Platform untuk tujuan ilegal atau tidak etis</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:prohibited-bold-duotone"></span>Pasal 3 - Konten yang Dilarang</h2>
    
    <h3>3.1 Kategori Konten Terlarang</h3>
    <p>Pengguna DILARANG KERAS untuk mengunggah, membagikan, atau mentransmisikan konten yang termasuk dalam kategori berikut:</p>

    <h3>3.2 Konten Kekerasan dan Ancaman</h3>
    <ul>
        <li>Konten yang memuat kekerasan fisik, verbal, atau psikologis terhadap individu atau kelompok;</li>
        <li>Ancaman kekerasan, intimidasi, atau terror terhadap Pengguna lain;</li>
        <li>Konten yang mengagungkan atau memuliakan kekerasan dan aksi kriminal;</li>
        <li>Instruksi atau panduan untuk melakukan tindakan kekerasan atau kriminal.</li>
    </ul>

    <h3>3.3 Ujaran Kebencian dan Diskriminasi</h3>
    <ul>
        <li>Konten yang menyerang atau mendiskriminasi individu atau kelompok berdasarkan ras, etnis, agama, jenis kelamin, orientasi seksual, disability, atau karakteristik lain yang dilindungi;</li>
        <li>Propaganda organisasi atau ideologi yang menganjurkan diskriminasi atau kekerasan;</li>
        <li>Meme, gambar, atau konten yang merendahkan kelompok tertentu.</li>
    </ul>

    <h3>3.4 Pelecehan dan Perundungan</h3>
    <ul>
        <li>Pelecehan verbal, fisik, atau psikologis terhadap Pengguna lain;</li>
        <li>Cyberbullying atau perundungan siber dalam bentuk apapun;</li>
        <li>Stalking atau pelacakan aktivitas Pengguna lain secara tidak sah;</li>
        <li>Distribusi informasi pribadi (doxing) tanpa persetujuan.</li>
    </ul>

    <h3>3.5 Konten Eksplisit dan Pornografi</h3>
    <ul>
        <li>Konten pornografi, seksual eksplisit, atau bertentangan dengan kesusilaan;</li>
        <li>Konten yang mengeksploitasi atau melibatkan anak-anak dalam konteks seksual;</li>
        <li>Konten dewasa yang tidak ditandai atau tidak sesuai untuk semua usia.</li>
    </ul>

    <h3>3.6 Konten Ilegal</h3>
    <ul>
        <li>Konten yang melanggar hukum Indonesia, termasuk namun tidak terbatas pada UU ITE dan KUHP;</li>
        <li>Instruksi untuk melakukan kejahatan atau aktivitas ilegal;</li>
        <li>Promosi atau distribusi zat berbahaya, senjata, atau barang ilegal;</li>
        <li>Konten terkait penipuan, skema cepat kaya, atau pyramid scheme.</li>
    </ul>

    <h3>3.7 Spam dan Manipulasi</h3>
    <ul>
        <li>Konten promosi atau iklan yang tidak diminta (spam);</li>
        <li>Konten yang dirancang untuk memanipulasi algoritma atau sistem Platform;</li>
        <li>Phishing, malware, virus, atau konten berbahaya lainnya;</li>
        <li>Konten clickbait yang menyesatkan atau tidak jujur.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:message-bold-duotone"></span>Pasal 4 - Ketentuan Khusus untuk Pesan Donasi</h2>
    
    <h3>4.1 Standar Pesan Donasi</h3>
    <p>Pesan donasi yang dikirimkan oleh Donatur harus memenuhi standar berikut:</p>
    <ul>
        <li>Tidak mengandung konten yang melanggar ketentuan dalam Pasal 3 Kebijakan ini;</li>
        <li>Tidak mengandung informasi pribadi orang lain tanpa persetujuan mereka;</li>
        <li>Tidak digunakan sebagai sarana untuk melanggar aturan platform atau hukum yang berlaku;</li>
        <li>Menggunakan bahasa dan kata-kata yang pantas dan sesuai untuk semua usia;</li>
        <li>Tidak berisi tautan eksternal yang mencurigakan atau berpotensi berbahaya.</li>
    </ul>

    <h3>4.2 Moderasi Pesan</h3>
    <p>Perusahaan berhak untuk:</p>
    <ul>
        <li>Memfilter atau memblokir pesan donasi yang mengandung kata-kata atau frasa yang melanggar;</li>
        <li>Menahan pesan donasi yang mencurigakan untuk review manual;</li>
        <li>Memberikan opsi kepada Creator untuk mengaktifkan filter kata-kata tertentu.</li>
    </ul>

