    <h2><span class="iconify" data-icon="solar:calculator-bold-duotone"></span>Pasal 1 - Pendahuluan dan Dasar Hukum</h2>
    <p>Kebijakan Perpajakan ini menjelaskan secara komprehensif tratamiento perpajakan yang berlaku untuk transaksi di Platform StreamDonate yang dioperasikan oleh PT VersaMorph Teknologi Indonesia ("Perusahaan"). Kebijakan ini disusun sebagai bentuk kepatuhan terhadap peraturan perpajakan yang berlaku di Republik Indonesia, termasuk namun tidak terbatas pada Undang-Undang Nomor 42 Tahun 2009 tentang Pajak Pertambahan Nilai Barang dan Jasa dan Pajak Penjualan atas Barang Mewah, Peraturan Menteri Keuangan yang berlaku, serta Undang-Undang Nomor 36 Tahun 2008 tentang Pajak Penghasilan.</p>

    <h3>1.1 Kepatuhan Perpajakan</h3>
    <p>Perusahaan berkomitmen untuk menjalankan kewajiban perpajakan sesuai dengan peraturan perundang-undangan yang berlaku dan menyediakan informasi perpajakan yang transparan kepada seluruh Pengguna Platform.</p>

    <h2><span class="iconify" data-icon="solar:heart-bold-duotone"></span>Pasal 2 - Perpajakan Donasi</h2>
    
    <h3>2.1 Status Perpajakan Donasi</h3>
    <p>Sesuai dengan regulasi perpajakan Indonesia, donasi yang diberikan melalui Platform StreamDonate diperlakukan sebagai berikut:</p>
    <ul>
        <li><strong>Donasi tidak dikenakan PPN:</strong> Donasi bersifat sebagai pemberian sukarela yang tidak termasuk dalam objek Pajak Pertambahan Nilai sesuai ketentuan UU PPN yang berlaku;</li>
        <li><strong>Tidak ada pemotongan pajak di sumber:</strong> Platform tidak melakukan pemotongan Pajak Penghasilan (PPh) terhadap donasi yang diterima oleh Creator.</li>
    </ul>

    <h3>2.2 Kewajiban Perpajakan Creator</h3>
    <p>Creator berkewajiban untuk:</p>
    <ul>
        <li>Melaporkan seluruh donasi yang diterima sebagai objek pajak penghasilan sesuai ketentuan perpajakan yang berlaku;</li>
        <li>Membayar Pajak Penghasilan (PPh) yang terutang atas pendapatan yang diterima dari donasi;</li>
        <li>Menyampaikan Surat Pemberitahuan (SPT) Tahunan Pajak Penghasilan tepat waktu kepada Direktorat Jenderal Pajak;</li>
        <li>Memenuhi kewajiban perpajakan lainnya sesuai dengan status wajib pajak masing-masing (individu atau badan usaha).</li>
    </ul>

    <h3>2.3 Catatan Penting</h3>
    <p>Donasi yang diterima oleh Creator merupakan pendapatan yang dapat dikategorikan sebagai penghasilan dari usaha atau penghasilan lainnya, tergantung pada status dan aktivitas Creator. Creator disarankan untuk:</p>
    <ul>
        <li>Konsultasi dengan konsultan pajak atau akuntan publik berlisensi untuk menentukan tratamiento pajak yang tepat;</li>
        <li>Mencatat dan mendokumentasikan seluruh donasi yang diterima untuk keperluan pelaporan pajak;</li>
        <li>Memperhatikan threshold kewajiban perpajakan sesuai regulasi yang berlaku.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:box-bold-duotone"></span>Pasal 3 - Perpajakan Produk Digital</h2>
    
    <h3>3.1 PPN atas Produk Digital</h3>
    <p>Sesuai dengan Peraturan Menteri Keuangan terkait perpajakan ekonomi digital, produk digital yang dijual melalui Platform StreamDonate dikenakan:</p>
    <ul>
        <li><strong>Pajak Pertambahan Nilai (PPN):</strong> Sebesar 11% dari harga jual produk digital (sesuai PMK terbaru tentang tarif PPN);</li>
        <li><strong>Harga sudah termasuk PPN:</strong> Seluruh harga produk digital yang ditampilkan di Platform sudah mencakup PPN, sehingga Pengguna tidak perlu membayar tambahan PPN secara terpisah;</li>
        <li><strong>Pencantuman PPN:</strong> PPN akan dicantumkan secara terpisah pada faktur pajak elektronik yang diterbitkan untuk setiap transaksi.</li>
    </ul>

    <h3>3.2 Kewajiban Perusahaan terkait PPN</h3>
    <p>Sebagai Pedagang E-commerce yang menjual produk digital, Perusahaan berkewajiban untuk:</p>
    <ul>
        <li>Mendaftarkan diri sebagai Pengusaha Kena Pajak (PKP) sesuai ketentuan yang berlaku;</li>
        <li>Memungut PPN atas penjualan produk digital kepada Pengguna;</li>
        <li>Menyetorkan PPN yang dipungut kepada kas negara tepat waktu sesuai jadwal pelaporan;</li>
        <li>Membuat dan menyerahkan Faktur Pajak Elektronik untuk setiap transaksi yang dikenakan PPN;</li>
        <li>Melaporkan PPN melalui SPT Masa PPN sesuai ketentuan DJP.</li>
    </ul>

    <h3>3.3 Faktur Pajak</h3>
    <p>Faktur pajak elektronik akan tersedia untuk setiap transaksi produk digital dan dapat diakses melalui:</p>
    <ul>
        <li>Dashboard akun Pengguna di menu "Riwayat Transaksi";</li>
        <li>Email konfirmasi yang dikirimkan setelah transaksi berhasil;</li>
        <li>Request khusus melalui email ke billing@tiptipan.id.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:document-check-bold-duotone"></span>Pasal 4 - Kewajiban Pelaporan</h2>
    
    <h3>4.1 Kewajiban Creator</h3>
    <p>Creator berkewajiban untuk:</p>
    <ul>
        <li>Mencatat seluruh pendapatan donasi yang diterima melalui Platform;</li>
        <li>Menghitung dan membayar PPh yang terutang sesuai tarif yang berlaku (5%, 15%, 25%, atau 30% tergantung penghasilan kena pajak);</li>
        <li>Menyampaikan SPT Tahunan Pajak Penghasilan paling lambat 3 (tiga) bulan setelah tahun pajak berakhir;</li>
        <li>Menyimpan bukti-bukti transaksi dan dokumentasi perpajakan selama minimal 10 (sepuluh) tahun.</li>
    </ul>

    <h3>4.2 Kewajiban Perusahaan</h3>
    <p>Perusahaan berkewajiban untuk:</p>
    <ul>
        <li>Menyediakan laporan transaksi yang dapat digunakan Creator untuk keperluan perpajakan;</li>
        <li>Memungut dan menyetorkan PPN atas produk digital;</li>
        <li>Memberikan bukti potong PPh jika diminta oleh Creator untuk transaksi tertentu;</li>
        <li>Menyediakan bukti-bukti transaksi yang diperlukan untuk keperluan audit pajak.</li>
    </ul>

