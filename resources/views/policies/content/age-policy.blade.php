    <h3>2.2 Dasar Penetapan Usia Minimum</h3>
    <p>Batasan usia minimum ditetapkan berdasarkan:</p>
    <ul>
        <li><strong>Pertimbangan Hukum:</strong> Kepatuhan terhadap regulasi Indonesia terkait usia legal untuk melakukan transaksi elektronik dan mengikat perjanjian;</li>
        <li><strong>Perlindungan Anak:</strong> Sesuai dengan Undang-Undang Nomor 35 Tahun 2014 tentang Perlindungan Anak, anak-anak di bawah 13 tahun memerlukan perlindungan khusus;</li>
        <li><strong>Kematangan Finansial:</strong> Pertimbangan kemampuan untuk memahami implikasi finansial dari transaksi;</li>
        <li><strong>Praktik Internasional:</strong> Referensi terhadap standar platform digital global (seperti COPPA di Amerika Serikat).</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:document-check-bold-duotone"></span>Pasal 3 - Verifikasi Usia</h2>
    
    <h3>3.1 Metode Verifikasi</h3>
    <p>Perusahaan dapat menerapkan metode verifikasi usia sebagai berikut:</p>
    <ul>
        <li><strong>Self-Declaration:</strong> Pengguna menyatakan usia mereka saat pendaftaran;</li>
        <li><strong>Verifikasi Dokumen:</strong> Perusahaan berhak untuk meminta dokumen verifikasi usia, seperti KTP, SIM, atau akta kelahiran;</li>
        <li><strong>Verifikasi Orang Tua:</strong> Untuk Pengguna di bawah 18 tahun, diperlukan verifikasi persetujuan dari orang tua atau wali yang sah;</li>
        <li><strong>Teknologi Verifikasi:</strong> Penggunaan teknologi age-gating untuk memvalidasi informasi usia.</li>
    </ul>

    <h3>3.2 Konsekuensi Informasi Palsu</h3>
    <p>Pengguna yang memberikan informasi usia yang palsu dapat menghadapi:</p>
    <ul>
        <li>Penangguhan atau penghapusan akun;</li>
        <li>Penolakan transaksi atau payout;</li>
        <li>Pengungkapan informasi kepada otoritas berwenang jika diperlukan.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:child-bold-duotone"></span>Pasal 4 - Ketentuan untuk Anak di Bawah 13 Tahun</h2>
    
    <h3>4.1 Kebijakan Khusus</h3>
    <p>Anak-anak yang berusia di bawah 13 tahun tidak diperkenankan untuk:</p>
    <ul>
        <li>Membuat akun sendiri di Platform;</li>
        <li>Melakukan transaksi keuangan dalam bentuk apapun;</li>
        <li>Menjadi Creator atau membuka halaman donasi;</li>
        <li>Mengunggah konten yang dapat dilihat publik.</li>
    </ul>

    <h3>4.2 Penggunaan di Bawah Pengawasan</h3>
    <p>Penggunaan Platform oleh anak di bawah 13 tahun hanya diperkenankan jika:</p>
    <ul>
        <li>Dilakukan di bawah pengawasan langsung orang tua atau wali yang sah;</li>
        <li>Orang tua atau wali membuat dan mengelola akun yang digunakan;</li>
        <li>Seluruh aktivitas dan transaksi dilakukan oleh orang tua atau wali.</li>
    </ul>

    <h3>4.3 Tanggung Jawab Orang Tua</h3>
    <p>Orang tua atau wali yang memberikan akses Platform kepada anak di bawah 13 tahun bertanggung jawab untuk:</p>
    <ul>
        <li>Mengawasi seluruh aktivitas anak di Platform;</li>
        <li>Memastikan tidak ada transaksi yang dilakukan tanpa persetujuan;</li>
        <li>Melaporkan konten atau perilaku yang tidak sesuai;</li>
        <li>Mengambil tanggung jawab atas seluruh aktivitas yang terjadi di akun.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:users-bold-duotone"></span>Pasal 5 - Ketentuan untuk Pengguna Usia 13-17 Tahun</h2>
    
    <h3>5.1 Hak dan Akses</h3>
    <p>Pengguna berusia 13-17 tahun dapat:</p>
    <ul>
        <li>Membuat dan mengelola akun sendiri dengan persetujuan orang tua/wali;</li>
        <li>Memberikan donasi melalui metode pembayaran yang sesuai;</li>
        <li>Menjadi Creator dan menerima donasi;</li>
        <li>Membeli produk digital dengan persetujuan orang tua/wali.</li>
    </ul>

    <h3>5.2 Pembatasan</h3>
    <p>Pengguna berusia 13-17 tahun tidak dapat:</p>
    <ul>
        <li>Melakukan payout atau menarik dana ke rekening sendiri;</li>
        <li>Menandatangani perjanjian mengikat secara hukum untuk dan atas nama entitas bisnis;</li>
        <li>Akses ke konten atau fitur yang dikategorikan untuk dewasa.</li>
    </ul>

    <h3>5.3 Verifikasi Persetujuan Orang Tua</h3>
    <p>Untuk dapat menggunakan Platform, Pengguna berusia 13-17 tahun harus:</p>
    <ul>
        <li>Menyediakan informasi kontak orang tua atau wali;</li>
        <li>Orang tua atau wali memberikan persetujuan eksplisit melalui email atau metode verifikasi lain;</li>
        <li>Orang tua atau wali dapat mencabut persetujuan kapan saja.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:shield-check-bold-duotone"></span>Pasal 6 - Hak Orang Tua dan Wali</h2>
    
    <h3>6.1 Hak yang Dimiliki</h3>
    <p>Orang tua atau wali dari Pengguna di bawah umur memiliki hak untuk:</p>
    <ul>
        <li>Membuat dan mengelola akun atas nama anak di bawah pengawasan mereka;</li>
        <li>Memantau aktivitas anak melalui dashboard akun;</li>
        <li>Meminta penghapusan akun dan seluruh data pribadi anak;</li>
        <li>Mencabut persetujuan kapan saja;</li>
        <li>Mengakses, memperbaiki, atau memperbarui informasi pribadi anak.</li>
    </ul>

    <h3>6.2 Permintaan Penghapusan Data</h3>
    <p>Orang tua atau wali dapat mengajukan permintaan penghapusan data pribadi anak dengan menghubungi:</p>
    <ul>
        <li><strong>Email:</strong> privacy@tiptipan.id dengan subjek "Permintaan Penghapusan Data Anak"</li>
        <li><strong>Telepon:</strong> +62 21 5678 9012</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:birthday-cake-bold-duotone"></span>Pasal 7 - Perubahan Status Usia</h2>
    
    <h3>7.1 Kenaikan Usia</h3>
    <p>Ketika Pengguna berusia 18 tahun atau lebih, Pengguna dapat:</p>
    <ul>
        <li>Mengajukan aktivasi fitur dewasa, termasuk penarikan dana;</li>
        <li>Menyelesaikan proses verifikasi KYC untuk membuka akses penuh;</li>
        <li>Mengelola rekening bank sendiri untuk payout.</li>
    </ul>

    <h3>7.2 Update Informasi</h3>
    <p>Pengguna wajib untuk:</p>
    <ul>
        <li>Memperbarui informasi tanggal lahir di profil akun;</li>
        <li>Menyelesaikan verifikasi tambahan yang diperlukan;</li>
        <li>Mengupdate informasi rekening bank jika diperlukan.</li>
    </ul>

