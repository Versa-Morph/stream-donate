    <h2><span class="iconify" data-icon="solar:box-bold-duotone"></span>Pasal 1 - Pendahuluan dan Ruang Lingkup</h2>
    <p>Kebijakan Produk Digital ini mengatur secara komprehensif ketentuan terkait pembelian, penjualan, penggunaan, dan pengelolaan produk digital yang tersedia di Platform StreamDonate yang dioperasikan oleh PT VersaMorph Teknologi Indonesia ("Perusahaan"). Kebijakan ini merupakan bagian tidak terpisahkan dari Ketentuan Layanan StreamDonate dan mengatur hubungan hukum antara Perusahaan, Creator, dan Pengguna terkait produk digital.</p>

    <h3>1.1 Definisi Produk Digital</h3>
    <p>Yang dimaksud dengan "Produk Digital" dalam kebijakan ini adalah konten, layanan, atau item virtual yang disediakan secara elektronik melalui Platform StreamDonate, termasuk namun tidak terbatas pada:</p>
    <ul>
        <li>Paket overlay streaming premium</li>
        <li>Tema alert dan animasi</li>
        <li>Widget premium dan komponen UI</li>
        <li>Alat analitik dan laporan</li>
        <li>Layanan subscription premium</li>
        <li>Konten digital eksklusif</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:shopping-bag-bold-duotone"></span>Pasal 2 - model bisnis Penjualan Produk Digital</h2>
    
    <h3>2.1 Kebijakan Penjualan</h3>
    <p>Perusahaan menetapkan kebijakan eksklusif terkait penjualan produk digital di Platform:</p>
    <ul>
        <li><strong>Eksklusivitas Platform:</strong> Seluruh produk digital dijual secara eksklusif melalui Platform StreamDonate;</li>
        <li><strong>Tidak Ada Penjualan Langsung oleh Creator:</strong> Creator tidak diperkenankan untuk menjual produk digital secara langsung di Platform atau melalui channel lain yang terkait dengan akun StreamDonate mereka;</li>
        <li><strong>Manajemen Sentral:</strong> Pembelian, distribusi, dan manajemen lisensi produk digital ditangani secara sentral oleh Perusahaan.</li>
    </ul>

    <h3>2.2 Katalog Produk Digital</h3>
    <p>Produk digital yang tersedia di Platform meliputi:</p>
    <ul>
        <li><strong>Paket Overlay Streaming:</strong> Template visual premium untuk overlay OBS dan streaming software lainnya;</li>
        <li><strong>Tema Alert:</strong> Desain animasi alert donasi dengan berbagai tema dan gaya;</li>
        <li><strong>Animasi Kustom:</strong> Animasi khusus untuk event stream, milestone, dan celebration;</li>
        <li><strong>Widget Premium:</strong> Komponen UI tambahan dengan fitur advanced;</li>
        <li><strong>Alat Analitik:</strong> Dashboard dan laporan statistik premium untuk Creator;</li>
        <li><strong>Subscription Premium:</strong> Akses bulanan/tahunan ke fitur dan konten premium.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:copy-bold-duotone"></span>Pasal 3 - Karakteristik Produk Digital</h2>
    
    <h3>3.1 Sifat Produk Digital</h3>
    <p>Produk digital memiliki karakteristik khusus yang membedakannya dari produk fisik:</p>
    <ul>
        <li><strong>Non-Fisik:</strong> Tidak ada pengiriman barang fisik; produk dikirimkan secara elektronik melalui Platform;</li>
        <li><strong>Instant Delivery:</strong> Produk dikirimkan langsung setelah pembayaran dikonfirmasi, biasanya dalam hitungan detik;</li>
        <li><strong>Non-Returnable dalam Kondisi Normal:</strong> Karena sifatnya yang digital, produk tidak dapat dikembalikan kecuali sesuai ketentuan refund dalam Kebijakan Pengembalian Dana;</li>
        <li><strong>Tidak Mudah Rusak:</strong> Produk digital tidak mengalami kerusakan fisik atau degradasi kualitas dari waktu.</li>
    </ul>

    <h3>3.2 Tipe Lisensi</h3>
    <p>Perusahaan menyediakan beberapa tipe lisensi untuk produk digital:</p>
    <ul>
        <li><strong>Lisensi Tunggal:</strong> Setiap lisensi terikat dengan satu akun Pengguna</li>
        <li><strong>Lisensi Tim:</strong> Lisensi yang dapat digunakan oleh tim dengan jumlah pengguna terbatas</li>
        <li><strong>Subscription:</strong> Akses sementara ke produk premium selama periode langganan aktif</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:key-bold-duotone"></span>Pasal 4 - Lisensi Penggunaan</h2>
    
    <h3>4.1 Cakupan Lisensi</h3>
    <p>Dengan membeli produk digital, Pengguna mendapatkan lisensi non-eksklusif, non-dapat dipindahtangankan, terbatas untuk penggunaan personal:</p>
    <ul>
        <li><strong>Personal Use Only:</strong> Produk hanya dapat digunakan oleh pemilik lisensi untuk keperluan pribadi atau komersial yang terkait dengan akun mereka;</li>
        <li><strong>Tidak untuk Komersialisasi Ulang:</strong> Pengguna tidak diperkenankan menjual, mendistribusikan, atau mengkomersialkan produk digital kepada pihak ketiga;</li>
        <li><strong>Tidak Dapat Dipindahkan:</strong> Lisensi tidak dapat dipindahkan antar akun atau dijual kepada Pengguna lain.</li>
    </ul>

    <h3>4.2 Pembatasan Lisensi</h3>
    <p>Pengguna DILARANG untuk:</p>
    <ul>
        <li>Menyalin, mereproduksi, atau mendistribusikan produk digital dalam bentuk apapun;</li>
        <li>Melakukan reverse engineering, decompiler, atau disassembler terhadap produk digital;</li>
        <li>Menjual kembali produk digital kepada pihak lain;</li>
        <li>Membuat karya turunan atau modifikasi dari produk digital;</li>
        <li>Menggunakan produk digital untuk melayani klien atau pihak ketiga;</li>
        <li>Memindahkan hak penggunaan kepada pihak lain, termasuk namun tidak terbatas pada gift, penjualan, atau sewa.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:copyright-bold-duotone"></span>Pasal 5 - Hak Kekayaan Intelektual</h2>
    
    <h3>5.1 Kepemilikan</h3>
    <p>Seluruh produk digital yang tersedia di Platform StreamDonate, termasuk namun tidak terbatas pada desain, animasi, kode sumber, dokumentasi, dan materi terkait lainnya, merupakan milik intelektual PT VersaMorph Teknologi Indonesia atau pihak ketiga yang telah memberikan lisensi kepada Perusahaan.</p>

    <h3>5.2 Perlindungan Hukum</h3>
    <p>Pelanggaran terhadap hak kekayaan intelektual dapat mengakibatkan:</p>
    <ul>
        <li>Pengakhiran lisensi secara immediately</li>
        <li>Penangguhan atau penghapusan akun</li>
        <li>Tuntutan ganti rugi materiel dan immateriel</li>
        <li>Proses hukum pidana dan perdata</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:users-bold-duotone"></span>Pasal 6 - Ketentuan untuk Creator</h2>
    
    <h3>6.1 Hak Creator terkait Produk Digital</h3>
    <p>Creator diperkenankan untuk:</p>
    <ul>
        <li>Membeli dan menggunakan produk digital Platform untuk keperluan streaming dan konten mereka sendiri;</li>
        <li>Menggunakan produk digital yang telah dibeli dalam konten streaming mereka;</li>
        <li>Mengajukan permintaan fitur baru atau peningkatan melalui channel yang tersedia.</li>
    </ul>

    <h3>6.2 Larangan bagi Creator</h3>
    <p>Creator DILARANG untuk:</p>
    <ul>
        <li>Menjual produk digital Platform kepada pihak lain dalam kapasitas apapun;</li>
        <li>Menyalin, mendistribusikan, atau membuat salinan produk digital untuk dijual;</li>
        <li>Membuat produk digital yang secara langsung bersaing dengan produk yang dijual di Platform;</li>
        <li>Mengkloning atau meniru konsep dan desain produk digital yang dilindungi.</li>
    </ul>

