        <li><strong>Telepon:</strong> +62 21 5678 9012 (24/7 Security Hotline)</li>
        <li><strong>Form Online:</strong> Melalui menu "Laporkan Fraud" di dashboard akun</li>
    </ul>

    <p>Semua laporan akan ditangani dengan kerahasiaan penuh dan ditindaklanjuti dalam waktu 1x24 jam.</p>

