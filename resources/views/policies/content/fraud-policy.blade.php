    <h2><span class="iconify" data-icon="solar:shield-warning-bold-duotone"></span>Pasal 1 - Pendahuluan dan Tujuan</h2>
    <p>Kebijakan Anti-Fraud ini merupakan dokumen resmi yang menguraikan komitmen PT VersaMorph Teknologi Indonesia ("Perusahaan") dalam mencegah, mendeteksi, dan menangani segala bentuk aktivitas penipuan yang terjadi di Platform StreamDonate. Kebijakan ini dibuat sebagai bentuk kepatuhan terhadap peraturan perundang-undangan yang berlaku di Republik Indonesia, termasuk namun tidak terbatas pada Undang-Undang Nomor 8 Tahun 2010 tentang Pencegahan dan Pemberantasan Tindak Pidana Pencucian Uang dan peraturan terkait lainnya.</p>

    <p>Tujuan kebijakan ini adalah untuk:</p>
    <ul>
        <li>Mewujudkan lingkungan transaksi yang aman dan terpercaya bagi seluruh Pengguna;</li>
        <li>Meminimalkan risiko kerugian finansial yang dialami oleh Donatur, Creator, dan Perusahaan;</li>
        <li>Menjaga integritas dan reputasi Platform StreamDonate;</li>
        <li>Mematuhi kewajiban pelaporan kepada otoritas berwenang sesuai regulasi yang berlaku.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:book-bold-duotone"></span>Pasal 2 - Definisi dan Klasifikasi Aktivitas Penipuan</h2>
    
    <h3>2.1 Definisi</h3>
    <p>Dalam kebijakan ini, yang dimaksud dengan "penipuan" atau "fraud" adalah setiap tindakan yang dilakukan secara sengaja untuk memperoleh keuntungan keuangan yang tidak sah melalui manipulasi, kebohongan, atau penyalahgunaan kepercayaan dalam konteks penggunaan Platform.</p>

    <h3>2.2 Klasifikasi Aktivitas Penipuan</h3>
    <ul>
        <li><strong>Carding:</strong> Penggunaan nomor kartu kredit, debit, atau kartu pembayaran lain yang diperoleh secara ilegal untuk melakukan transaksi di Platform;</li>
        <li><strong>Identity Theft:</strong> Penggunaan identitas orang lain tanpa otorisasi untuk membuat akun atau melakukan transaksi;</li>
        <li><strong>Chargeback Fraud:</strong> Permintaan pengembalian dana yang tidak sah kepada bank atau lembaga keuangan setelah transaksi berhasil diproses;</li>
        <li><strong>Fake Donation:</strong> Pembuatan transaksi donasi fiktif untuk memanipulasi statistik, leaderboard, atau konten alert;</li>
        <li><strong>Account Takeover:</strong> Pengambilalihan akun pengguna lain melalui phishing, social engineering, atau metode lainnya;</li>
        <li><strong>Money Laundering:</strong> Penggunaan Platform untuk menyembunyikan atau menyamarkan asal-usul dana yang diperoleh dari aktivitas ilegal;</li>
        <li><strong>Sycophant Fraud:</strong> Penggunaan bot, skrip otomatis, atau kolusi pengguna untuk mendapatkan posisi tidak sah di leaderboard.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:search-bold-duotone"></span>Pasal 3 - Sistem Pencegahan dan Deteksi</h2>
    
    <h3>3.1 Verifikasi Transaksi</h3>
    <p>Perusahaan menerapkan serangkaian langkah verifikasi untuk memastikan keaslian setiap transaksi:</p>
    <ul>
        <li><strong>3D Secure Authentication:</strong> Verifikasi tambahan untuk transaksi kartu kredit/debit melalui protokol Visa Secure, Mastercard Identity Check, atau JCB J/Secure;</li>
        <li><strong>CVV Verification:</strong> Validasi kode keamanan tiga digit di belakang kartu;</li>
        <li><strong>Address Verification System (AVS):</strong> pencocokan alamat penagihan dengan data yang terdaftar di bank penerbit;</li>
        <li><strong>OTP (One-Time Password):</strong> Verifikasi melalui kode sekali pakai yang dikirimkan ke nomor telepon terdaftar.</li>
    </ul>

    <h3>3.2 Analisis Risiko Real-Time</h3>
    <p>Sistem kami secara otomatis menganalisis setiap transaksi menggunakan machine learning dan rule-based engine untuk mendeteksi pola mencurigakan:</p>
    <ul>
        <li><strong>Velocity Check:</strong> Deteksi jumlah transaksi yang tidak wajar dalam periode waktu tertentu;</li>
        <li><strong>Geolocation Analysis:</strong> Analisis lokasi geografis untuk mendeteksi anomali;</li>
        <li><strong>Device Fingerprinting:</strong> Identifikasi perangkat untuk mendeteksi akun atau kartu yang digunakan di multiple perangkat;</li>
        <li><strong>Behavioral Analysis:</strong> Pemantauan pola perilaku pengguna untuk mendeteksi deviasi dari kebiasaan normal.</li>
    </ul>

    <h3>3.3 Indikator Aktivitas Mencurigakan</h3>
    <p>Sistem kami flags transaksi atau akun yang menunjukkan karakteristik berikut:</p>
    <ul>
        <li>Multiple transaksi dari alamat IP yang sama dalam waktu singkat;</li>
        <li>Jumlah donasi yang tidak proporsional dengan profil pengguna atau pola historis;</li>
        <li>Penggunaan metode pembayaran yang berbeda-beda dari akun yang sama;</li>
        <li>Transaksi dari wilayah geografis yang diketahui sebagai high-risk;</li>
        <li>Pola aktivitas yang konsisten dengan bot atau script otomatis;</li>
        <li>Request penarikan dana segera setelah menerima donasi dalam jumlah besar.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:gavel-bold-duotone"></span>Pasal 4 - Tindakan dan Sanksi</h2>
    
    <h3>4.1 Tindakan Immediately</h3>
    <p>Ketika aktivitas penipuan terdeteksi, Perusahaan akan mengambil tindakan segera berikut:</p>
    <ul>
        <li><strong>Freeze Dana:</strong> Penahanan sementara dana yang terkait dengan transaksi mencurigakan, dengan periode penahanan maksimal 180 hari selama proses investigasi;</li>
        <li><strong>Account Suspension:</strong> Penangguhan sementara akses akun yang terkait untuk mencegah kerugian lebih lanjut;</li>
        <li><strong>Transaction Hold:</strong> Pembatalan transaksi yang belum settle dan refund otomatis untuk Donatur;</li>
        <li><strong>Investigation:</strong> Proses investigasi internal yang melibatkan tim keamanan dan, jika diperlukan, koordinasi dengan pihak berwenang.</li>
    </ul>

