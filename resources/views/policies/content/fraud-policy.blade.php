    <h3>4.3 Ganti Rugi</h3>
    <p>Pengguna yang terbukti melakukan penipuan dapat dikenakan kewajiban ganti rugi yang mencakup:</p>
    <ul>
        <li>Jumlah total transaksi penipuan yang dilakukan;</li>
        <li>Biaya administrasi dan investigasi yang dikeluarkan oleh Perusahaan;</li>
        <li>Kerugian tidak langsung yang dialami oleh pihak-pihak yang terdampak;</li>
        <li>Biaya hukum yang dikeluarkan untuk proses hukum terkait.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:shield-check-bold-duotone"></span>Pasal 5 - Pelaporan dan Kerja Sama dengan Otoritas</h2>
    
    <h3>5.1 Kewajiban Pelaporan</h3>
    <p>Perusahaan berkomitmen untuk melaporkan setiap indikasi tindak pidana kepada pihak berwenang sesuai ketentuan:</p>
    <ul>
        <li><strong>UU Pencucian Uang:</strong> Laporan Transaksi Keuangan Mencurigakan (LTKM) ke PPATK;</li>
        <li><strong>UU ITE:</strong> Laporan kepada Direktorat Siber Bareskrim POLRI untuk kasus terkait kejahatan siber;</li>
        <li><strong>OJK:</strong> Pelaporan sesuai kewajiban sebagai Penyelenggara Sistem Elektronik.</li>
    </ul>

    <h3>5.2 Kontribusi Investigasi</h3>
    <p>Perusahaan akan memberikan bantuan penuh dalam proses investigasi yang dilakukan oleh pihak berwenang, termasuk namun tidak terbatas pada penyediaan data transaksi, informasi akun, dan rekam jejak digital.</p>

