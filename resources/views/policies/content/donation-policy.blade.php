    <h2><span class="iconify" data-icon="solar:heart-bold-duotone"></span>Pasal 1 - Ketentuan Umum Donasi</h2>
    <p>Kebijakan Donasi ini merupakan bagian integral dari Ketentuan Layanan StreamDonate dan mengatur seluruh aspek terkait pengiriman, penerimaan, serta pengelolaan donasi yang dilakukan melalui Platform StreamDonate yang dioperasikan oleh PT VersaMorph Teknologi Indonesia ("Perusahaan"). Dengan memberikan atau menerima donasi melalui Platform, baik Donatur maupun Creator menyatakan memahami dan menyetujui seluruh ketentuan dalam kebijakan ini.</p>

    <h3>1.1 Definisi Donasi</h3>
    <p>Donasi yang dimaksud dalam kebijakan ini adalah transfer dana sukarela dari Donatur ke Creator yang dilakukan melalui Platform StreamDonate, dengan karakteristik sebagai berikut:</p>
    <ul>
        <li><strong>Sukarela:</strong> Memberikan donasi merupakan tindakan yang sepenuhnya sukarela, tanpa paksaan atau tekanan dalam bentuk apapun dari pihak manapun;</li>
        <li><strong>Non-Refundable dalam Kondisi Normal:</strong> Setelah donasi berhasil diproses dan dikonfirmasi, dana tidak dapat dikembalikan kecuali dalam kasus-kasus khusus yang diatur dalam Kebijakan Pengembalian Dana;</li>
        <li><strong>Tanpa Imbalan Langsung:</strong> Donasi tidak dimaksudkan untuk menciptakan hubungan kontraktual atau pertukaran barang dan/atau jasa secara langsung antara Donatur dan Creator;</li>
        <li><strong>Non-Kontraktual:</strong> Donasi tidak menciptakan hubungan kontraktual antara Donatur dan Perusahaan, kecuali untuk aspek pemrosesan pembayaran.</li>
    </ul>

