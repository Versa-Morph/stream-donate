    <h2><span class="iconify" data-icon="solar:bolt-bold-duotone"></span>Pasal 4 - Mekanisme Pemrosesan Donasi</h2>
    
    <h3>4.1 Alur Pemrosesan</h3>
    <p>Donasi melalui Platform StreamDonate diproses melalui tahapan sebagai berikut:</p>
    <ol>
        <li><strong>Inisiasi:</strong> Donatur memilih jumlah donasi dan metode pembayaran di halaman donasi Creator;</li>
        <li><strong>Input Data:</strong> Donatur mengisi detail donasi, termasuk pesan dukungan (opsional) dan informasi/media sosial (opsional);</li>
        <li><strong>Verifikasi Pembayaran:</strong> Sistem memverifikasi detail pembayaran dengan payment gateway atau bank terkait;</li>
        <li><strong>Konfirmasi:</strong> Donatur menerima konfirmasi pembayaran melalui email dan/atau SMS;</li>
        <li><strong>Notifikasi Real-time:</strong> Alert donasi dikirim ke sistem Creator melalui SSE (Server-Sent Events) dalam hitungan detik;</li>
        <li><strong>Queue Management:</strong> Donasi masuk ke antrean dan ditampilkan secara berurutan sesuai waktu penerimaan;</li>
        <li><strong>Transfer Dana:</strong> Dana donasi (setelah dipotong biaya) ditransfer ke rekening Creator sesuai jadwal pencairan.</li>
    </ol>

    <h3>4.2 Waktu Pemrosesan</h3>
    <ul>
        <li><strong>E-Wallet & Kartu:</strong> Proses instan, dana langsung masuk ke sistem dan alert terkirim dalam 1-5 detik;</li>
        <li><strong>Transfer Bank:</strong> Tergantung bank, umumnya 1-60 menit pada jam operasional;</li>
        <li><strong>Gerai Retail:</strong> Umumya 30-60 menit setelah konfirmasi pembayaran di gerai.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:prohibited-bold-duotone"></span>Pasal 5 - Larangan dan Pembatasan</h2>
    <p>Semua Pengguna DILARANG KERAS untuk melakukan tindakan-tindakan berikut dalam konteks donasi:</p>
    <ul>
        <li><strong>Penggunaan Alat Pembayaran Orang Lain:</strong> Menggunakan kartu kredit, rekening bank, atau e-wallet milik orang lain tanpa otorisasi eksplisit dari pemilik sah;</li>
        <li><strong>Pencucian Uang:</strong> Menggunakan Platform untuk menyembunyikan atau menyamarkan asal-usul dana yang diperoleh dari kegiatan ilegal;</li>
        <li><strong>Donasi Palsu:</strong> Membuat donasi fiktif atau menggunakan bot/automasi untuk memanipulasi statistik donasi;</li>
        <li><strong>Manipulasi Leaderboard:</strong> Menggunakan akun siluman atau kolusi untuk meningkatkan ranking di leaderboard;</li>
        <li><strong>Paksaan Donasi:</strong> Menerapkan tekanan, ancaman, atau manipulasi psikologis untuk memaksa orang memberikan donasi;</li>
        <li><strong>Fraud Sistem Pembayaran:</strong> Menggunakan kartu kredit/debit yang dicuri, rekening kosong, atau metode pembayaran penipuan lainnya.</li>
    </ul>

