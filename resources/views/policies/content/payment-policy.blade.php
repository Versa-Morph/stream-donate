    <h2><span class="iconify" data-icon="solar:credit-card-bold-duotone"></span>Pasal 1 - Ruang Lingkup dan Ketentuan Umum</h2>
    <p>Kebijakan Pembayaran ini merupakan bagian integral dari Ketentuan Layanan StreamDonate dan mengatur seluruh aspek terkait metode pembayaran yang tersedia, struktur biaya transaksi, mekanisme payout untuk Creator, serta hak dan kewajiban para pihak dalam konteks pembayaran di Platform StreamDonate yang dioperasikan oleh PT VersaMorph Teknologi Indonesia ("Perusahaan").</p>

    <h2><span class="iconify" data-icon="solar:wallet-bold-duotone"></span>Pasal 2 - Metode Pembayaran yang Didukung</h2>
    
    <h3>2.1 Kartu Elektronik</h3>
    <p>Platform StreamDonate mendukung pembayaran menggunakan berbagai jenis kartu elektronik untuk memberikan fleksibilitas maksimal kepada Donatur:</p>
    <ul>
        <li><strong>Kartu Kredit:</strong> Visa, Mastercard, JCB, American Express, dan UnionPay International;</li>
        <li><strong>Kartu Debit:</strong> Semua kartu debit berlogo Visa Electron, Maestro, dan Mastercard yang mendukung transaksi online;</li>
        <li><strong>Kartu Prepaid:</strong> Beberapa kartu prepaid yang mendukung transaksi online dengan verifikasi 3D Secure, seperti BCA Flazz, Mandiri e-Money, dan sejenisnya.</li>
    </ul>

    <h3>2.2 Dompet Elektronik (E-Wallet)</h3>
    <p>Untuk kemudahan transaksi, Platform mendukung berbagai dompet elektronik yang populer di Indonesia:</p>
    <ul>
        <li>GoPay (Gojek)</li>
        <li>OVO (Lippo)</li>
        <li>DANA (Dana Indonesia)</li>
        <li>LinkAja (Telkomsel)</li>
        <li>ShopeePay (Shopee)</li>
        <li>BCA Mobile</li>
        <li>Octo Mobile (CIMB)</li>
