    <h2><span class="iconify" data-icon="solar:building-bold-duotone"></span>Pasal 4 - Mekanisme Payout untuk Creator</h2>
    
    <h3>4.1 Jadwal Pencairan Dana</h3>
    <p>Dana donasi yang telah masuk ke rekening escrow Perusahaan akan dicairkan ke rekening Creator sesuai jadwal berikut:</p>
    <ul>
        <li><strong>Request Manual:</strong> Pencairan dilakukan setiap hari kerja (Senin-Jumat) dalam 1x24 jam setelah request diverifikasi;</li>
        <li><strong>Minimum Payout:</strong> Rp 10.000 untuk satu kali pencairan;</li>
        <li><strong>Hari Libur:</strong> Request yang masuk di hari Sabtu, Minggu, atau hari libur nasional akan diproses pada hari kerja berikutnya.</li>
    </ul>

    <h3>4.2 Hold Period</h3>
    <p>Untuk keamanan dan pencegahan penipuan, Perusahaan menerapkan periode penahanan (hold period) sebagai berikut:</p>
    <ul>
        <li><strong>Akun Baru (0-90 hari):</strong> Hold period 7 hari untuk setiap transaksi;</li>
        <li><strong>Akun Terverifikasi Penuh:</strong> Hold period 3 hari;</li>
        <li><strong>Akun Premium/Trusted:</strong> Hold period 1 hari atau instant payout.</li>
    </ul>

    <h3>4.3 Metode Penarikan Dana</h3>
    <p>Creator dapat menarik dana menggunakan metode berikut:</p>
    <ul>
        <li><strong>Transfer Bank:</strong> BCA, BRI, BNI, Mandiri, BTPN (gratis untuk beberapa bank, ada biaya transfer untuk bank lain)</li>
        <li><strong>E-Wallet:</strong> GoPay, OVO (biasanya instant dan tanpa biaya)</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:document-check-bold-duotone"></span>Pasal 5 - Syarat dan Ketentuan Payout</h2>
    
    <h3>5.1 Syarat Umum</h3>
    <p>Creator wajib memenuhi syarat-syarat berikut untuk dapat melakukan penarikan dana:</p>
    <ul>
        <li>Akun telah terverifikasi secara penuh melalui proses KYC (Know Your Customer);</li>
        <li>Minimal berusia 18 tahun atau memiliki persetujuan tertulis dari orang tua/wali untuk usia di bawah 18 tahun;</li>
        <li>Telah menyelesaikan seluruh dokumen verifikasi yang diperlukan;</li>
        <li>Tidak dalam status penangguhan atau investigasi apapun.</li>
    </ul>

    <h3>5.2 Informasi Rekening</h3>
    <p>Creator bertanggung jawab untuk memastikan:</p>
    <ul>
        <li>Nama pemilik rekening bank/e-wallet sesuai dengan nama yang terdaftar di akun StreamDonate;</li>
        <li>Nomor rekening, kode bank, atau nomor e-wallet yang angegeben benar dan aktif;</li>
        <li>Memperbarui informasi rekening jika terjadi perubahan dalam waktu 1x24 jam.</li>
    </ul>

