    <h2><span class="iconify" data-icon="solar:rewind-back-bold-duotone"></span>Pasal 1 - Ketentuan Umum Pengembalian Dana</h2>
    <p>Kebijakan Pengembalian Dana ("Kebijakan Refund") ini mengatur secara komprehensif prosedur, syarat, dan ketentuan terkait pengembalian dana yang dapat diajukan oleh Pengguna Platform StreamDonate yang dioperasikan oleh PT VersaMorph Teknologi Indonesia ("Perusahaan"). Kebijakan ini merupakan bagian tidak terpisahkan dari Ketentuan Layanan dan Kebijakan Privasi yang telah disetujui oleh Pengguna.</p>

    <h3>1.1 Prinsip Dasar</h3>
    <p>Perusahaan berkomitmen untuk menyediakan layanan donasi digital yang transparan dan adil. Namun, mengingat sifat khusus dari layanan donasi yang bersifat sukarela dan non-refundable dalam kondisi normal, Perusahaan menetapkan ketentuan refund yang ketat untuk melindungi kepentingan semua pihak yang terlibat dalam ekosistem Platform.</p>

    <h2><span class="iconify" data-icon="solar:heart-bold-duotone"></span>Pasal 2 - Kebijakan Refund untuk Donasi</h2>
    
    <h3>2.1 Prinsip Non-Refundable</h3>
    <p>Donasi yang berhasil diproses melalui Platform StreamDonate bersifat <strong>final dan non-refundable</strong>, dengan pertimbangan sebagai berikut:</p>
    <ul>
        <li><strong>Sifat Sukarela:</strong> Donasi merupakan kontribusi sukarela yang diberikan atas kebebasan dan kemauan tanpa paksaan dari Donatur;</li>
        <li><strong>Penerimaan Langsung:</strong> Dana donasi langsung ditransfer ke rekening Creator setelah dipotong biaya Platform, sehingga secara teknis tidak memungkinkan untuk menarik kembali dana yang telah diterima;</li>
        <li><strong>Dukungan Konten:</strong> Donasi dimaksudkan sebagai bentuk dukungan sukarela untuk pembuatan konten kreatif oleh Creator.</li>
    </ul>

    <h3>2.2 Pengecualian untuk Donasi</h3>
    <p>Meskipun prinsip umum adalah non-refundable, Perusahaan dapat mempertimbangkan pengembalian dana untuk donasi dalam kondisi luar biasa berikut:</p>
    <ul>
        <li><strong>Kesalahan Teknis:</strong> Jika donasi berhasil diproses namun tidak tercatat di sistem atau terjadi duplikasi transaksi karena bug platform;</li>
        <li><strong>Transaksi Tidak Sah:</strong> Jika donasi dilakukan menggunakan akun yang diretas atau metode pembayaran yang tidak diotorisasi oleh pemilik sah;</li>
        <li><strong>Pelanggaran Kebijakan:</strong> Jika donasi dilakukan dalam konteks yang melanggar Ketentuan Layanan, termasuk namun tidak terbatas pada donasi yang dipaksakan.</li>
    </ul>

    <h3>2.3 Batas Waktu Pengajuan</h3>
    <p>Pengajuan refund untuk donasi harus dilakukan dalam periode sebagai berikut:</p>
    <ul>
        <li><strong>Kesalahan Teknis:</strong> Maksimal 7 (tujuh) hari kalender sejak tanggal transaksi;</li>
        <li><strong>Transaksi Tidak Sah:</strong> Maksimal 30 (tiga puluh) hari kalender sejak tanggal transaksi;</li>
        <li><strong>Chargeback Bank:</strong> Harap diperhatikan bahwa chargeback yang tidak sah dapat mengakibatkan penangguhan atau penghapusan akun.</li>
    </ul>

