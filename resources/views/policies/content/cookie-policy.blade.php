    <h2><span class="iconify" data-icon="solar:list-bold-duotone"></span>Pasal 3 - Teknologi Pelacakan Lainnya</h2>
    
    <h3>3.1 Local Storage dan Session Storage</h3>
    <p>Selain cookie, kami juga menggunakan Local Storage dan Session Storage HTML5 untuk menyimpan informasi di perangkat Anda:</p>
    <ul>
        <li><strong>Local Storage:</strong> Menyimpan data yang persist bahkan setelah browser ditutup;</li>
        <li><strong>Session Storage:</strong> Menyimpan data sementara yang dihapus ketika sesi browser berakhir.</li>
    </ul>

    <h3>3.2 Pixel Tags (Web Beacons)</h3>
    <p>Kami menggunakan pixel tags (juga dikenal sebagai web beacons atau clear GIFs) yang merupakan gambar grafik kecil yang diletakkan di halaman website atau email. Pixel tags memungkinkan kami untuk:</p>
    <ul>
        <li>Menghitung jumlah pengunjung yang mengakses halaman tertentu;</li>
        <li>Memantau pola traffic pengguna di Platform;</li>
        <li>Mengetahui apakah email telah dibuka oleh penerima.</li>
    </ul>

    <h3>3.3 Fingerprinting</h3>
    <p>Kami tidak menggunakan teknik fingerprinting perangkat untuk mengidentifikasi pengguna secara unik tanpa persetujuan eksplisit.</p>

    <h2><span class="iconify" data-icon="solar:settings-bold-duotone"></span>Pasal 4 - Pengelolaan Cookie</h2>
    
    <h3>4.1 Pilihan Anda</h3>
    <p>Anda memiliki kontrol penuh atas cookie dan preferensi pelacakan Anda:</p>
    <ul>
        <li><strong>Cookie Consent Manager:</strong> Anda dapat mengatur preferensi cookie melalui Cookie Consent Manager yang tersedia di Platform;</li>
        <li><strong>Pengaturan Browser:</strong> Anda dapat mengatur browser untuk menolak atau menghapus cookie tertentu;</li>
        <li><strong>Opt-out Analytics:</strong> Anda dapat memilih untuk tidak ikut dalam analitik melalui pengaturan preferensi kami.</li>
    </ul>

    <h3>4.2 Cara Menonaktifkan Cookie</h3>
    <p>Jika Anda ingin menonaktifkan cookie, Anda dapat melakukannya melalui pengaturan browser Anda:</p>
    <ul>
        <li><strong>Chrome:</strong> Settings > Privacy and Security > Cookies and site data</li>
        <li><strong>Firefox:</strong> Options > Privacy & Security > Cookies and Site Data</li>
        <li><strong>Safari:</strong> Preferences > Privacy > Cookies</li>
        <li><strong>Edge:</strong> Settings > Privacy & Services > Cookies</li>
    </ul>

    <h3>4.3 Konsekuensi Penonaktifan</h3>
    <p>Harap diperhatikan bahwa menonaktifkan cookie tertentu dapat mempengaruhi fungsionalitas Platform. Misalnya:</p>
    <ul>
        <li>Menonaktifkan cookie yang diperlukan dapat menyebabkan Anda tidak dapat login;</li>
        <li>Menonaktifkan cookie preferensi akan mereset preferensi Anda setiap kali kunjungan;</li>
        <li>Menonaktifkan cookie analitik akan mempengaruhi kemampuan kami untuk meningkatkan layanan.</li>
    </ul>

