    <h3>1.1 Apa itu Cookie?</h3>
    <p>Cookie adalah file teks kecil yang disimpan di perangkat Anda (komputer, tablet, atau ponsel) ketika Anda mengunjungi atau menggunakan sebuah website. Cookie digunakan secara luas untuk memastikan pengoperasian website yang efisien, memberikan pengalaman pengguna yang lebih baik, dan memungkinkan pemilik website untuk memahami bagaimana pengunjung menggunakan situs mereka.</p>

    <h3>1.2 Mengapa Kami Menggunakan Cookie?</h3>
    <p>Kami menggunakan cookie dan teknologi serupa untuk berbagai tujuan, termasuk:</p>
    <ul>
        <li>Memastikan pengoperasian teknis Platform yang tepat;</li>
        <li>Mengingat preferensi dan pengaturan Anda;</li>
        <li>Menganalisis bagaimana Platform digunakan untuk meningkatkan kualitas layanan;</li>
        <li>Menampilkan konten dan iklan yang relevan untuk Anda;</li>
        <li>Meningkatkan keamanan Platform.</li>
    </ul>

