    <h2><span class="iconify" data-icon="solar:copyright-bold-duotone"></span>Pasal 1 - Pendahuluan dan Dasar Hukum</h2>
    <p>Kebijakan Hak Kekayaan Intelektual ini merupakan dokumen resmi yang mengatur perlindungan, kepemilikan, dan penggunaan atas seluruh Hak Kekayaan Intelektual (HKI) yang terkait dengan Platform StreamDonate yang dioperasikan oleh PT VersaMorph Teknologi Indonesia ("Perusahaan"). Kebijakan ini disusun sebagai bentuk kepatuhan terhadap Undang-Undang Nomor 28 Tahun 2014 tentang Hak Cipta, Undang-Undang Nomor 20 Tahun 2016 tentang Merek dan Indikasi Geografis, serta peraturan pelaksanaan terkait lainnya.</p>

    <h2><span class="iconify" data-icon="solar:building-bold-duotone"></span>Pasal 2 - Kepemilikan HKI Platform</h2>
    
    <h3>2.1 lingkup Kepemilikan</h3>
    <p>Seluruh Hak Kekayaan Intelektual yang terkait dengan Platform StreamDonate merupakan milik eksklusif PT VersaMorph Teknologi Indonesia atau pihak ketiga yang telah memberikan lisensi yang sah kepada Perusahaan. lingkup kepemilikan ini meliputi namun tidak terbatas pada:</p>
    <ul>
        <li><strong>Software dan Kode Sumber:</strong> Seluruh kode program, algoritma, database, API, dan komponen teknologi yang membentuk Platform;</li>
        <li><strong>Desain dan Tampilan (UI/UX):</strong> Tata letak, skema warna, tipografi, elemen grafis, animasi, dan seluruh aspek desain visual Platform;</li>
        <li><strong>Logo dan Identitas Visual:</strong> Merek dagang "StreamDonate", "Tiptipan", logo, ikon, maskot, dan seluruh elemen identitas brand;</li>
        <li><strong>Dokumentasi:</strong> Manual pengguna, dokumentasi teknis, artikel, blog, dan seluruh materi tertulis yang terkait dengan Platform;</li>
        <li><strong>Konten Platform:</strong> Template, widget, alert designs, overlay, dan seluruh konten digital yang disediakan oleh Platform;</li>
        <li><strong>Merek Dagang:</strong> Seluruh merek dagang terdaftar dan tidak terdaftar yang digunakan oleh Perusahaan.</li>
    </ul>

    <h3>2.2 Perlindungan Hukum</h3>
    <p>Hak-hak tersebut dilindungi oleh undang-undang hak cipta dan merek dagang yang berlaku. Perusahaan berhak untuk mengambil tindakan hukum yang diperlukan untuk melindungi hak-haknya.</p>

    <h2><span class="iconify" data-icon="solar:prohibited-bold-duotone"></span>Pasal 3 - Larangan Penggunaan</h2>
    
    <h3>3.1 Aktiv yang Dilarang</h3>
    <p>Tanpa persetujuan tertulis dari Perusahaan, Pengguna DILARANG untuk:</p>
    <ul>
        <li><strong>Menyalin atau Mendistribusikan:</strong> Menyalin, mereproduksi, mendistribusikan, menjual, atau menyediakan akses ke kode sumber, desain, atau materi lain milik Platform kepada pihak manapun;</li>
        <li><strong>Reverse Engineering:</strong> Melakukan decompiler, disassembler, atau aktivitas reverse engineering lainnya terhadap software atau teknologi Platform;</li>
        <li><strong>Karya Turunan:</strong> Membuat karya turunan, modifikasi, adaptasi, atau derivative work dari konten atau teknologi Platform;</li>
        <li><strong>Penggunaan Merek:</strong> Menggunakan, menampilkan, atau mengkloning logo, merek dagang, atau elemen identitas visual Perusahaan untuk tujuan apapun;</li>
        <li><strong>Klaim Kepemilikan:</strong> Mengklaim atau menyiratkan kepemilikan atas materi atau hak apapun yang dimiliki oleh Perusahaan;</li>
        <li><strong>Pencurian Konten:</strong> Mengambil, scraping, atau mengekstrak konten, data, atau informasi dari Platform dengan cara yang melanggar Ketentuan Layanan.</li>
    </ul>

    <h3>3.2 Konsekuensi Pelanggaran</h3>
    <p>Pelanggaran terhadap ketentuan dalam pasal ini dapat mengakibatkan:</p>
    <ul>
        <li>Pengakhiran immediately atas akses ke Platform;</li>
        <li>Penangguhan atau penghapusan permanen akun;</li>
        <li>Tuntutan ganti rugi materiel dan immateriel;</li>
        <li>Proses hukum pidana dan perdata sesuai ketentuan UU Hak Cipta dan UU Merek.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:user-bold-duotone"></span>Pasal 4 - Hak Kekayaan Intelektual Pengguna</h2>
    
    <h3>4.1 Konten yang Dibuat oleh Creator</h3>
    <p>Creator yang mengunggah atau membuat konten di Platform (seperti profil, deskripsi, banner, overlay custom, dan materi visual original) tetap memegang hak cipta atas konten tersebut. Namun, dengan mengunggah konten ke Platform, Creator memberikan lisensi non-eksklusif kepada Perusahaan untuk:</p>
    <ul>
        <li>Menyimpan dan menampilkan konten tersebut melalui Platform;</li>
        <li>Menggunakan konten untuk keperluan promosi Platform;</li>
        <li>Mengubah atau memodifikasi konten sesuai kebutuhan teknis Platform;</li>
        <li>Menzolkan konten kepada pihak ketiga dalam konteks operasi Platform.</li>
    </ul>

    <h3>4.2 Konten yang Dilarang</h3>
    <p>Creator DILARANG untuk mengunggah konten yang:</p>
    <ul>
        <li>Melanggar hak cipta, merek dagang, atau hak kekayaan intelektual pihak ketiga;</li>
        <li>Mengandung materi berhak cipta yang digunakan tanpa izin pemilik yang sah;</li>
        <li>Melanggar hak privasi atau publisitas orang lain.</li>
    </ul>

    <h2><span class="iconify" data-icon="solar:shield-warning-bold-duotone"></span>Pasal 5 - Pelanggaran Hak Kekayaan Intelektual</h2>
    
    <h3>5.1 Larangan Pelanggaran</h3>
    <p>Perusahaan melarang keras tindakan-tindakan berikut:</p>
    <ul>
        <li>Pelanggaran hak cipta pihak ketiga, termasuk namun tidak terbatas pada penggunaan konten berhak cipta tanpa izin;</li>
        <li>Pelanggaran merek dagang, termasuk penggunaan merek yang mirip atau identik dengan merek milik pihak lain;</li>
        <li>Pencurian ide atau konsep yang dipresentasikan melalui Platform;</li>
        <li>Penggunaan konten yang dilindungi tanpa atribusi atau izin yang diperlukan.</li>
    </ul>

    <h3>5.2 Notifikasi Pelanggaran (DMCA Notice)</h3>
    <p>Perusahaan menghargai dan melindungi hak kekayaan intelektual seluruh pihak. Jika Anda yakin bahwa hak cipta atau HKI Anda telah dilanggar melalui Platform, silakan kirimkan Notifikasi Penghapusan sesuai ketentuan DMCA atau regulasi terkait ke:</p>
    <ul>
        <li><strong>Email:</strong> copyright@tiptipan.id</li>
        <li><strong>Subject:</strong> "Laporan Pelanggaran Hak Cipta - [Nama Konten]"</li>
    </ul>

    <h3>5.3 Informasi yang Harus Disertakan</h3>
    <p>Notifikasi pelanggaran harus memuat:</p>
    <ul>
        <li>Identifikasi karya cipta atau HKI yang alleged infringe;</li>
        <li>Identifikasi materi yang alleged infringe;</li>
        <li>Informasi kontak Anda (nama, alamat, email, nomor telepon);</li>
        <li>Surat jaminan bahwa Anda memiliki hak untuk bertindak atas nama pemilik hak cipta;</li>
        <li>Tanda tangan fisik atau elektronik yang sah.</li>
    </ul>

